#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
    if (n >= 10)
    {
        ft_putnbr(n / 10);
        ft_putnbr(n % 10);
    }
    else
        ft_putchar_fd(n + 48, fd);
}
